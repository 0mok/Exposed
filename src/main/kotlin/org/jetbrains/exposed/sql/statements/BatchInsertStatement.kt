package org.jetbrains.exposed.sql.statements

import org.jetbrains.exposed.sql.Column
import org.jetbrains.exposed.sql.ColumnType
import org.jetbrains.exposed.sql.Table
import org.jetbrains.exposed.sql.Transaction
import org.jetbrains.exposed.sql.vendors.H2Dialect
import org.jetbrains.exposed.sql.vendors.currentDialect
import java.sql.PreparedStatement
import java.util.*

class BatchInsertStatement(val table: Table, val _ignore: Boolean = false): Statement<List<Any>>(StatementType.INSERT, listOf(table)) {

    val data = ArrayList<LinkedHashMap<Column<*>, Any?>>()

    fun addBatch() {
        data.add(LinkedHashMap())
        arguments = null
    }

    operator fun <T> set(column: Column<T>, value: T) {
        val values = data.last()

        if (values.containsKey(column)) {
            error("$column is already initialized")
        }

        values.put(column, column.columnType.valueToDB(value))
    }

    private var arguments: Iterable<Iterable<Pair<Column<*>, Any?>>>? = null
        get() {
            return field ?: listOf(data.map {
                single -> table.columns.filterNot { it.columnType.autoinc }.map { it to (single[it] ?: it.defaultValueFun?.invoke()) }
            }.flatten()).apply { field = this }
        }

    override fun arguments(): Iterable<Iterable<Pair<ColumnType, Any?>>> = arguments!!.map { it.map { it.first.columnType to it.second } }

    override fun prepareSQL(transaction: Transaction): String {
        if (data.isNotEmpty()) {
            val columns = table.columns.filterNot { it.columnType.autoinc }

            val values = buildString {
                append("VALUES ")
                val paramsPlaceholder = columns.map { "?" }.joinToString(", ", prefix = "(", postfix = "),")

                append(paramsPlaceholder.repeat(data.size).removeSuffix(","))
            }

            return transaction.db.dialect.insert(_ignore, table, columns, values, transaction)
        }

        error("No data provided to insert")
    }

    override fun PreparedStatement.executeInternal(transaction: Transaction): List<Any> {
        if (data.isEmpty()) return emptyList()

        val count = executeUpdate()

        assert(_ignore || count == data.size) { "Number of results don't match number of entries in batch" }

        val autoGeneratedKeys = arrayListOf<Any>()

        if (table.columns.any { it.columnType.autoinc }) {
            val rs = generatedKeys!!
            while (rs.next()) {
                autoGeneratedKeys.add(rs.getObject(1))
            }

            if (count > 1 && currentDialect == H2Dialect) {
                // H2 only returns one last generated keys...
                (autoGeneratedKeys.singleOrNull() as? Long)?.let {
                    var id = it

                    while (autoGeneratedKeys.size < count) {
                        id -= 1
                        autoGeneratedKeys.add(0, id)
                    }
                } ?: (autoGeneratedKeys.singleOrNull() as? Int)?.let {
                    var id = it

                    while (autoGeneratedKeys.size < count) {
                        id -= 1
                        autoGeneratedKeys.add(0, id)
                    }
                }
            }

            assert(_ignore || autoGeneratedKeys.isEmpty() || autoGeneratedKeys.size == count) { "Number of autoincs doesn't match number of batch entries" }
        } else if(table.columns.any { it.indexInPK != null && it.defaultValueFun != null }) {
            arguments!!.flatMap{ it }.filter { it.first.indexInPK != null && it.second != null }.mapTo(autoGeneratedKeys) { it.second!! }
        }
        return autoGeneratedKeys
    }

}
